#include "libft.h"

char	*ft_itoa(int nb)
{
	int		i[2];
	char	*s;

	if (nb == -2147483648 || !(s = (char *)ft_memalloc(sizeof(char) * 12)))
		return (nb == -2147483648 ? ft_strdup("-2147483648") : NULL);
	i[0] = (nb < 0);
	i[1] = nb;
	s[0] = (nb >= 0 ? '0' * (nb == 0) : '-');
	while (i[1])
		i[1] /= 10 + (0 * ++(i[0]));
	while (--(i[0]) >= !!s[0] && (s[i[0]] = '0' + (nb < 0 ? -nb : nb) % 10))
		nb /= 10;
	return (s);
}
